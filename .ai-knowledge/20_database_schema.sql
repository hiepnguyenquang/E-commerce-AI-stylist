-- =====================================================================
-- 1. AI PROFILE
-- =====================================================================
-- Used by:
--   apps/api-medusa/src/api/v1/ai-profile/route.ts
--
-- Behavior:
--   The route implements an upsert-like flow by customer_id. The schema does
--   not declare customer_id as UNIQUE, therefore uniqueness is enforced by
--   application behavior rather than a database constraint.

CREATE TABLE IF NOT EXISTS "ai_profile" (
    "id" text NOT NULL,
    "customer_id" text NOT NULL,
    "height" numeric NULL,
    "weight" numeric NULL,
    "base_body_image_url" text NULL,
    "raw_height" jsonb NULL,
    "raw_weight" jsonb NULL,
    "gender" text NULL,
    "created_at" timestamptz NOT NULL DEFAULT now(),
    "updated_at" timestamptz NOT NULL DEFAULT now(),
    "deleted_at" timestamptz NULL,
    CONSTRAINT "ai_profile_pkey" PRIMARY KEY ("id")
);

CREATE INDEX IF NOT EXISTS "IDX_ai_profile_deleted_at"
ON "ai_profile" ("deleted_at")
WHERE "deleted_at" IS NULL;

COMMENT ON TABLE "ai_profile" IS
'Custom Medusa module table for user AI profile data. customer_id is a logical reference to Medusa customer/auth actor.';

COMMENT ON COLUMN "ai_profile"."raw_height" IS
'Generated by Medusa bigNumber handling for height.';

COMMENT ON COLUMN "ai_profile"."raw_weight" IS
'Generated by Medusa bigNumber handling for weight.';

-- =====================================================================
-- 2. USER CLOSET ITEM
-- =====================================================================
-- Used by:
--   apps/api-medusa/src/api/v1/user/garments/route.ts
--   apps/api-medusa/src/api/v1/user/garments/[id]/route.ts
